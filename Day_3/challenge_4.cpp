// Write a program to assign the value of one variable to another
#include<iostream>
using namespace std;

int main()
{
	// create and print the age variable
	int age = 20;
	cout << age << endl;

	// create another variable age2
	int age2 = 21;

	// assign the value of age2 to age
	age = age2;
	
	//print age
	cout << age << endl;

	return 0;

}
