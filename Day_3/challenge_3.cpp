// Write a program to change values of variables

#include<iostream>

using namespace std;

int main()
{
	//create marks variable and assign 630 to it
	int marks = 630; // int for integer type
	
	//print the marks variable
	cout << marks << endl;

	//assign 650 to marks
	marks = 650;

	//print the marks variable again in a new line
	cout << marks << endl;

	return 0;

}
